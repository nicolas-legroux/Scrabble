package com.github.nlegroux.scrabble;

enum ScrabbleLetter {
    A('A'),
    B('B'),
    C('C'),
    D('D'),
    E('E'),
    F('F'),
    G('G'),
    H('H'),
    I('I'),
    J('J'),
    K('K'),
    L('L'),
    M('M'),
    N('N'),
    O('O'),
    P('P'),
    Q('Q'),
    R('R'),
    S('S'),
    T('T'),
    U('U'),
    V('V'),
    W('W'),
    X('X'),
    Y('Y'),
    Z('Z'),
    WILDCARD(' ');

    private final char character;

    ScrabbleLetter(char character) {
        this.character = character;
    }

    boolean isWildcard() {
        return this == ScrabbleLetter.WILDCARD;
    }

    @Override
    public String toString() {
        return character + "";
    }
}
